export const replacePlaceholders = (
    template: string,
    car: { name: string; brand: string } | null,
    condition: { sale_type: string; pay_type: string; initial_deposit: number; delivery_time: string; colors: string[] } | null,
    business: { dealership_name?: string; address?: string; contact_phones?: string; mobile_numbers?: string } | null
): string => {
    let result = template;

    const carName = car ? car.name : "خودرو";
    const carBrand = car ? car.brand : "";
    
    let conditionType = "شرایط فروش ویژه";
    if (condition) {
        conditionType = `${condition.sale_type} (${condition.pay_type})`;
    }
    
    let depositText = "مبلغ توافقی";
    if (condition && condition.initial_deposit > 0) {
        const depositInTomans = condition.initial_deposit;
        if (depositInTomans >= 1000000) {
            depositText = `${(depositInTomans / 1000000).toLocaleString('fa-IR')} میلیون تومان`;
        } else {
            depositText = `${depositInTomans.toLocaleString('fa-IR')} تومان`;
        }
    }

    const deliveryTime = condition ? condition.delivery_time : "تحویل فوری";
    const colorsText = condition && condition.colors && condition.colors.length > 0 ? condition.colors.join('، ') : "رنگ‌های مجاز";
    
    const phone = business?.mobile_numbers || business?.contact_phones || "شماره تماس نمایندگی";
    const dealershipName = business?.dealership_name || "نمایندگی رسمی حسینی خودرو";
    const address = business?.address || "تهران، دفتر مرکزی نمایندگی";

    result = result.replace(/\[خودرو\]/g, carName);
    result = result.replace(/\[برند\]/g, carBrand);
    result = result.replace(/\[شرایط\]/g, conditionType);
    result = result.replace(/\[پیش_پرداخت\]/g, depositText);
    result = result.replace(/\[تحویل\]/g, deliveryTime);
    result = result.replace(/\[رنگ\]/g, colorsText);
    result = result.replace(/\[تلفن\]/g, phone);
    result = result.replace(/\[نام_نمایشگاه\]/g, dealershipName);
    result = result.replace(/\[آدرس\]/g, address);

    return result;
};
